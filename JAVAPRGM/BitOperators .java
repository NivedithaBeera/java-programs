public class BitOperators {
    public static void main(String[] args) {
     byte a = 10, b = 15;
    System.out.printf("%d & %d = %d\n", a, b, a & b);
    System.out.printf("%d | %d = %d\n", a, b, a | b);
    System.out.printf("%d >> 2 = %d\n", a, a >> 2);
    System.out.printf("%d << 2 = %d\n", a, a << 2);
    }
 }